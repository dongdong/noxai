_lang_list = ['en', 'zh', 'ko', 'ja']


def _get_all_tag_names():
    all_tag_name_set = set()
    for item in _tag_config_list:     
        for lang in _lang_list:
            if lang in item:
                all_tag_name_set.add(item[lang])
    return all_tag_name_set


def _get_tag_structure_id_map():
    tag_name_id_map = {}
    for item in _tag_config_list:    
        if 'structure_tag_id' in item:
            tag_id = item['structure_tag_id'] 
            for lang in _lang_list:
                if lang in item:
                    tag_name = item[lang]
                    tag_name_id_map[tag_name] = tag_id
    return tag_name_id_map


def get_tag_name_trans_map(from_lang, to_lang):
    tag_name_trans_map = {}
    for item in _tag_config_list:
        if from_lang in item:
            from_name = item[from_lang]
            if to_lang in item:
                to_name = item[to_lang]
            else:
                to_name = from_name
            tag_name_trans_map[from_name] = to_name
    return tag_name_trans_map


def get_tag_local_name_map(language):
    tag_local_name_map = get_tag_name_trans_map('en', language) 
    #print(tag_local_name_map)
    return tag_local_name_map   


def check_tags():
    from kol_models.youtube_tags.train.tag_video_config import TagVideoConfig
    from kol_models.youtube_tags.models.youtube_topic_tags import get_all_tag_names as get_all_ytb_tag_names
    from kol_models.youtube_tags.models.tag_match_model import get_all_tags as get_all_match_tag_names 
    from kol_models.youtube_tags.models.default_match_model import get_all_tags as get_all_default_match_tag_names 

    all_tag_name_set = _get_all_tag_names()

    for lang in _lang_list:
        tag_video_config = TagVideoConfig.Load(lang)
        for level_1_tag in tag_video_config.get_level_1_tags():
            if level_1_tag != '' and level_1_tag not in all_tag_name_set:
                print(level_1_tag, lang)
            for level_2_tag in tag_video_config.get_level_2_tags(level_1_tag):
                if level_2_tag != '' and level_2_tag not in all_tag_name_set:
                    print(level_2_tag, lang)

    ytb_tag_names = get_all_ytb_tag_names()
    for tag_name in ytb_tag_names:
        if tag_name not in all_tag_name_set:
            print(tag_name, 'ytb')

    match_tag_names = get_all_match_tag_names()
    for tag_name in match_tag_names:
        if tag_name not in all_tag_name_set:
            print(tag_name, 'tag_match_model')

    match_tag_names = get_all_default_match_tag_names()
    for tag_name in match_tag_names:
        if tag_name not in all_tag_name_set:
            print(tag_name, 'default_tag_match_model')


if __name__ == '__main__':
    check_tags()




